    private void jBtalterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtalterarMouseClicked
        // TODO add your handling code here:
        if (jTableDisciplina.getSelectedRow() != -1){

            Disciplina d = new Disciplina(0);
            
            DisciplinaDAO dd = new DisciplinaDAO();
            
            d.setDescricao(jTXTDescricao.getText());
            d.setEmenta(jTXTEmenta.getText());
            d.setLimiteVaga(Integer.parseInt(jTXTLimVagas.getText()));
            d.setProfessor(cbProfessor.getSelectedIndex()+1);
            d.setCodigo((int) jTableDisciplina.getValueAt(jTableDisciplina.getSelectedRow(), 0));
            d.setDiaSemana(cbDiaSemana.getSelectedItem().toString());
            
            try {
                dd.atualizadisciplina(d);
                Listar();
            } catch (SQLException ex) {
                Logger.getLogger(CadDisciplina.class.getName()).log(Level.SEVERE, null, ex);
            }
            clean();
        }
    }//GEN-LAST:event_jBtalterarMouseClicked

    private void jBtexcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtexcluirMouseClicked
        // TODO add your handling code here:
        
         if (jTableDisciplina.getSelectedRow() != -1){

            Disciplina d = new Disciplina(0);
            
            DisciplinaDAO dd = new DisciplinaDAO();
            
            d.setCodigo((int) jTableDisciplina.getValueAt(jTableDisciplina.getSelectedRow(), 0));

            try {
                dd.deletadisciplina(d);
            } catch (SQLException ex) {
                Logger.getLogger(CadDisciplina.class.getName()).log(Level.SEVERE, null, ex);
            }

            try {
                Listar();
                
            } catch (SQLException ex) {
                Logger.getLogger(CadAluno.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jBtexcluirMouseClicked

