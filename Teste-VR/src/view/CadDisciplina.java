    private void jBtIncluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtIncluirMouseClicked
        // TODO add your handling code here:
        
        Disciplina d = new Disciplina(jTXTDescricao.getText(),
                                      jTXTEmenta.getText(),
                                      Integer.parseInt(jTXTLimVagas.getText()),
                                      cbProfessor.getSelectedIndex()+1,
                                      cbDiaSemana.getSelectedItem().toString(),
                                      Integer.parseInt(jTXTCargaHoraria.getText()));
        
        DisciplinaDAO dd = new DisciplinaDAO();
        try {
            dd.insereDisciplina(d);
            
            Listar();
        } catch (SQLException ex) {
            Logger.getLogger(CadDisciplina.class.getName()).log(Level.SEVERE, null, ex);
        }
        clean();
        
    }//GEN-LAST:event_jBtIncluirMouseClicked
    
    private void refresh(){
    if (jTableDisciplina.getSelectedRow() != -1){
            
            jTXTDescricao.setText(jTableDisciplina.getValueAt(jTableDisciplina.getSelectedRow(), 1).toString());
            jTXTEmenta.setText(jTableDisciplina.getValueAt(jTableDisciplina.getSelectedRow(), 2).toString());
            jTXTLimVagas.setText(jTableDisciplina.getValueAt(jTableDisciplina.getSelectedRow(), 3).toString());
            cbDiaSemana.setSelectedItem(jTableDisciplina.getValueAt(jTableDisciplina.getSelectedRow(), 4).toString());
        }
    }
    
