    public void Listar() throws SQLException{
    
        DefaultTableModel modelo = (DefaultTableModel) jTableAluno.getModel();
        AlunoDAO ad = new AlunoDAO();
        modelo.setRowCount(0);
        
        for (Aluno a : ad.read()) {
            
             modelo.addRow(new Object[]{
                a.getCodigo(),
                a.getMatricula(),
                a.getNome(),
                a.getRg(),
                a.getCpf(),
                a.getCurso()
             }
             );   
        }
    }

