    private void jBTAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBTAlterarMouseClicked
        // TODO add your handling code here:
        
        if (jTableAluno.getSelectedRow() != -1){

            Aluno a = new Aluno(0);
            
            AlunoDAO ad = new AlunoDAO();
            a.setMatricula(jTxtmatricula.getText());
            a.setNome(jtxtNome.getText());
            a.setRg(jTXTRG.getText());
            a.setCpf(jTXTCPF.getText());
            a.setCodigo((int) jTableAluno.getValueAt(jTableAluno.getSelectedRow(), 0));
            a.setCurso(cbCurso.getSelectedItem().toString());
            
            try {
                ad.atualizaAluno(a);
                Listar();
            } catch (SQLException ex) {
                Logger.getLogger(CadAluno.class.getName()).log(Level.SEVERE, null, ex);
            }
            clean();
        }
    }//GEN-LAST:event_jBTAlterarMouseClicked

