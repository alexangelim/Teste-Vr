    private void jBtInserirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtInserirMouseClicked
        // TODO add your handling code here:
        Aluno a = new Aluno(jTxtmatricula.getText(),
                            jtxtNome.getText(),
                            jTXTCPF.getText(),
                            jTXTRG.getText(),
                            cbCurso.getSelectedItem().toString());
        
        AlunoDAO ad = new AlunoDAO();
        
        try {
            ad.insereAluno(a, a.getCurso());
            
            Listar();
        } catch (SQLException ex) {
            Logger.getLogger(CadAluno.class.getName()).log(Level.SEVERE, null, ex);
        }
        clean();
             
    }//GEN-LAST:event_jBtInserirMouseClicked

    private void jBTexcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBTexcluirMouseClicked
        // TODO add your handling code here:
        
        if (jTableAluno.getSelectedRow() != -1){

            Aluno a = new Aluno(0);
            
            AlunoDAO ad = new AlunoDAO();
            
            a.setCodigo((int) jTableAluno.getValueAt(jTableAluno.getSelectedRow(), 0));

            try {
                ad.deletaAluno(a);
            } catch (SQLException ex) {
                Logger.getLogger(CadAluno.class.getName()).log(Level.SEVERE, null, ex);
            }

            try {
                Listar();
                
            } catch (SQLException ex) {
                Logger.getLogger(CadAluno.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jBTexcluirMouseClicked

