    private void inicio() throws SQLException {   
       Listar();
       CarregaCombo();
       cbCurso.setSelectedIndex(-1);
    }

    private void Refresh() {
        if (jTableAluno.getSelectedRow() != -1){
            
            jTxtmatricula.setText(jTableAluno.getValueAt(jTableAluno.getSelectedRow(), 1).toString());
            jtxtNome.setText(jTableAluno.getValueAt(jTableAluno.getSelectedRow(), 2).toString());
            jTXTRG.setText(jTableAluno.getValueAt(jTableAluno.getSelectedRow(), 3).toString());
            jTXTCPF.setText(jTableAluno.getValueAt(jTableAluno.getSelectedRow(), 4).toString());
            cbCurso.setSelectedItem(jTableAluno.getValueAt(jTableAluno.getSelectedRow(), 5).toString());
        }
    }
    
    private void CarregaCombo() throws SQLException{
        DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) cbCurso.getModel();
        CursoDAO cd = new CursoDAO();
        
        for (model.Curso c : cd.ListarCombo()){ 
            comboBoxModel.addElement(c.getCodigo() + " - " +  c.getDescricao());    
        }
    }

